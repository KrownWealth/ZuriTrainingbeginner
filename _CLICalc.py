#Allow user to input value
number_1 = int(input("Enter the first number"))
number_2 = int(input("Enter the second number"))

#Addition
print(number_1 + number_2)

#Subtraction
print(number_1 - number_2)

#Division
print(number_1 / number_2)

#Multiplication
print(number_1 * number_2)

#Modulus
print(number_1 % number_2)